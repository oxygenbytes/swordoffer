/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode(int x) : val(x), next(NULL) {}
 * };
 */
class Solution {
public:
    ListNode* getKthFromEnd(ListNode* head, int k) {
        if(!head) return nullptr;

        ListNode* pre = head;
        while(k--){
            if(pre) pre = pre->next;
            else return nullptr;
        }

        ListNode* cur = head;
        while(pre){
            cur = cur->next;
            pre = pre->next;
        }
        return cur;
    }
};
