class Solution {
public:
    Node* copyRandomList(Node* head) {
        if(!head) return nullptr;

        auto p = head;
        Node* q;

        while(p){
            q = p->next;
            p->next = new Node(p->val);
            p->next->next =q;
            p = q;
        }

        Node* cp = nullptr;
        p = head;

        while(p){
            cp = p->next;
            q = p->next->next;
            cp->random = p->random ? p->random->next : nullptr;
            p = q;
        }

        Node* res = head->next;
        p = head;

        while(p){
            cp = p->next;
            q = p->next->next;
            p->next = q;
            cp->next = q ? q->next : nullptr;
            p = q;
        }

        return res;
    }
};
